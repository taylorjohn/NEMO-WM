# ── CLI ───────────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    import argparse, sys

    ap = argparse.ArgumentParser(description="CORTEX-PE domain registry")
    ap.add_argument("command", choices=["list", "show", "template", "validate"],
                    help="list: show all domains | show: full spec | "
                         "template: print new domain template | validate: check spec")
    ap.add_argument("--domain", default=None)
    args = ap.parse_args()

    if args.command == "list":
        print(f"\nCORTEX-PE Domain Registry  ({len(REGISTRY)} domains)\n")
        for name, spec in REGISTRY.items():
            status = "✅" if spec.baseline_result and spec.baseline_result >= spec.primary_metric_target else "🔄"
            print(f"  {status}  {name:<12}  {spec.primary_metric}>{spec.primary_metric_target:.2f}  "
                  f"baseline={spec.baseline_result or 'none':<6}  {spec.description[:50]}")
        print()

    elif args.command == "show":
        if not args.domain:
            ap.error("--domain required for show")
        spec = REGISTRY.get(args.domain)
        if not spec:
            print(f"Domain '{args.domain}' not found. Available: {list(REGISTRY)}")
            sys.exit(1)
        print(spec.summary())

    elif args.command == "template":
        print(new_domain_template())

    elif args.command == "validate":
        if args.domain:
            specs = [REGISTRY[args.domain]] if args.domain in REGISTRY else []
        else:
            specs = list(REGISTRY.values())
        all_ok = True
        for spec in specs:
            errors = spec.validate()
            if errors:
                print(f"❌ {spec.name}: {errors}")
                all_ok = False
            else:
                print(f"✅ {spec.name}: OK")
        if all_ok:
            print("\nAll specs valid.")
