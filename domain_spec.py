"""domain_spec.py — CORTEX-PE v16.17
═══════════════════════════════════════════════════════════════════════════════
Domain specification and registry.

A DomainSpec fully describes a new domain to the CORTEX-PE system — enough
for the scaffold generator to build training + eval scripts, and for the
intervention executor to know how to improve them.

Adding a new domain:
  1. Create a DomainSpec (see examples at the bottom of this file)
  2. Register it: REGISTRY["my_domain"] = my_spec
  3. Run: python domain_scaffold.py --domain my_domain
  4. The system generates train_my_domain.py + eval_my_domain.py
  5. First run is committed to lineage automatically
  6. Subsequent improvements are driven by the intervention table

Modalities supported:
  "image"      — RGB frames, StudentEncoder backbone
  "audio"      — 1-D waveform or spectrogram, BEATs/wav2vec2 teacher
  "telemetry"  — multivariate time series, SubspaceAD head
  "tabular"    — structured feature vectors, PCA + anomaly scoring

Heads available per modality:
  image:     RoPETemporalHead, QuasimetricHead, SubspaceAD, ClassifierHead
  audio:     ProjectionHead, SubspaceAD, ClassifierHead
  telemetry: SubspaceAD, IsolationForest, ClassifierHead
  tabular:   SubspaceAD, IsolationForest
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path
from typing import Any


# ─────────────────────────────────────────────────────────────────────────────
# Core spec
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class DomainSpec:
    """Complete specification of a CORTEX-PE domain.

    Parameters
    ----------
    name : str
        Short identifier used in filenames and lineage keys. e.g. "recon".
    description : str
        One-sentence description of the task.
    modality : str
        Input modality: "image", "audio", "telemetry", "tabular".
    data_path : str
        Path to the dataset root on the NUC. Relative to CORTEX dir.
    data_format : str
        File format: "hdf5", "wav", "csv", "npy", "folder_of_images".
    data_notes : str
        Key layout, gotchas, decode requirements.
        e.g. "images/rgb_left stores JPEG bytes as fixed-length HDF5 strings"
    primary_metric : str
        The metric that defines improvement. e.g. "auroc", "triplet_ordering".
    primary_metric_target : float
        Success threshold. Training stops recommending interventions above this.
    teacher : str | None
        Pre-trained teacher model, if any. e.g. "dinov2_small", "beats".
    head : str
        Head architecture for the downstream task.
        e.g. "RoPETemporalHead", "SubspaceAD", "ProjectionHead".
    loss : str
        Training objective. e.g. "triplet_infonce", "distillation", "subspace".
    eval_script : str
        Name of the evaluation script. Generated by scaffold if not existing.
    checkpoint_dir : str
        Where checkpoints are saved. e.g. "checkpoints/recon_student".
    interventions : list[str]
        Ordered list of intervention tags to try when plateau is detected.
        These must map to entries in the InterventionExecutor.
    baseline_result : float | None
        Known baseline result before any training, for sanity checking.
    notes : str
        Any additional context, known issues, or hardware requirements.
    extra : dict
        Domain-specific parameters passed to the scaffold generator.
        e.g. {"k_near_max": 5, "k_far_min": 15, "t_max": 70}
    """
    name:                   str
    description:            str
    modality:               str
    data_path:              str
    data_format:            str
    primary_metric:         str
    primary_metric_target:  float
    head:                   str
    loss:                   str
    eval_script:            str
    checkpoint_dir:         str
    interventions:          list[str]
    data_notes:             str       = ""
    teacher:                str | None = None
    baseline_result:        float | None = None
    notes:                  str       = ""
    extra:                  dict      = field(default_factory=dict)

    def validate(self) -> list[str]:
        """Return list of validation errors (empty = OK)."""
        errors = []
        valid_modalities = {"image", "audio", "telemetry", "tabular"}
        valid_formats    = {"hdf5", "wav", "csv", "npy", "folder_of_images", "pt"}
        valid_heads      = {"RoPETemporalHead", "SubspaceAD", "ProjectionHead",
                           "QuasimetricHead", "ClassifierHead", "MLPHead",
                           "IsolationForest"}
        valid_losses     = {"triplet_infonce", "infonce", "distillation",
                           "subspace", "crossentropy", "mse"}

        if self.modality not in valid_modalities:
            errors.append(f"modality '{self.modality}' not in {valid_modalities}")
        if self.data_format not in valid_formats:
            errors.append(f"data_format '{self.data_format}' not in {valid_formats}")
        if self.head not in valid_heads:
            errors.append(f"head '{self.head}' not in {valid_heads}")
        if self.loss not in valid_losses:
            errors.append(f"loss '{self.loss}' not in {valid_losses}")
        if not self.name.replace("_", "").isalnum():
            errors.append(f"name '{self.name}' must be alphanumeric + underscore")
        if not self.interventions:
            errors.append("interventions list must not be empty")
        return errors

    def summary(self) -> str:
        lines = [
            f"Domain: {self.name}",
            f"  Description:  {self.description}",
            f"  Modality:     {self.modality}  ({self.data_format})",
            f"  Data:         {self.data_path}",
            f"  Teacher:      {self.teacher or 'none (train from scratch)'}",
            f"  Head:         {self.head}",
            f"  Loss:         {self.loss}",
            f"  Target:       {self.primary_metric} > {self.primary_metric_target}",
            f"  Checkpoint:   {self.checkpoint_dir}",
            f"  Interventions ({len(self.interventions)}): "
            + ", ".join(self.interventions[:4])
            + ("..." if len(self.interventions) > 4 else ""),
        ]
        if self.notes:
            lines.append(f"  Notes:        {self.notes}")
        return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# Domain registry — all known domains
# ─────────────────────────────────────────────────────────────────────────────

REGISTRY: dict[str, DomainSpec] = {}


def register(spec: DomainSpec) -> DomainSpec:
    errors = spec.validate()
    if errors:
        raise ValueError(f"DomainSpec '{spec.name}' invalid:\n" + "\n".join(errors))
    REGISTRY[spec.name] = spec
    return spec


